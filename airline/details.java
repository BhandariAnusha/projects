void table_update(){
    int c;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/airport1","root","");
            pst=con.prepareStatement("select * from details");
            rs=pst.executeQuery();
            rss=rs.getMetaData();
            c=rss.getColumnCount();
            DefaultTableModel df=(DefaultTableModel) jTable1.getModel();
            df.setRowCount(0);
            while(rs.next()){
                Vector v=new Vector();
                for(int a=0;a<c;a++){
                    v.add(rs.getString("sn"));
                    v.add(rs.getString("name"));
                    v.add(rs.getString("nationality"));
                    v.add(rs.getString("ctzno"));
                    v.add(rs.getString("gender"));
                    v.add(rs.getString("age"));
                    v.add(rs.getString("from"));
                    v.add(rs.getString("destination"));
                    v.add(rs.getString("flightno"));
                    v.add(rs.getString("date"));
                    v.add(rs.getString("seat"));
                    v.add(rs.getString("time"));
                    v.add(rs.getString("cls"));
                }
                df.addRow(v);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(details.class.getName()).log(Level.SEVERE, null, ex);
        }
}
