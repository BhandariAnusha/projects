    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        DefaultTableModel df=(DefaultTableModel) jTable1.getModel();
        int SelectedIndex=jTable1.getSelectedRow();
        name.setText(df.getValueAt(SelectedIndex, 1).toString());
        nationality.setText(df.getValueAt(SelectedIndex, 2).toString());
        ctzno.setText(df.getValueAt(SelectedIndex, 3).toString());
        gender.setText(df.getValueAt(SelectedIndex, 4).toString());
        age.setText(df.getValueAt(SelectedIndex, 5).toString());
        from.setText(df.getValueAt(SelectedIndex, 6).toString());
        destination.setText(df.getValueAt(SelectedIndex, 7).toString());
        flightno.setText(df.getValueAt(SelectedIndex, 8).toString());
        date.setText(df.getValueAt(SelectedIndex, 9).toString());
        seat.setText(df.getValueAt(SelectedIndex, 10).toString());
        time.setText(df.getValueAt(SelectedIndex, 11).toString());
        cls.setSelectedItem(df.getValueAt(SelectedIndex, 12).toString());
        sn.setText(df.getValueAt(SelectedIndex, 0).toString());
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost/airport1","root","");
            pst=con.prepareStatement("SELECT `name`, `nationality`, `ctzno`, `gender`, `age`, `from`, `destination`, "
                    + "`flightno`, `date`, `seat`, `time`, `cls`, `profile` FROM `details` WHERE sn=?");
            pst.setString(1, sn.getText());
            rs=pst.executeQuery();
            if(rs.next()){
                profile.setIcon(new ImageFunction().resizePic(null, rs.getBytes("profile"), profile.getWidth(), profile.getHeight()));
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(details.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

